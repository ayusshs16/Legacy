const mongoose = require('mongoose');

const familySchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    members: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
      },
    ],
  },
  {
    timestamps: true,
  }
);

const Family = mongoose.model('Family', familySchema);

module.exports = Family; 